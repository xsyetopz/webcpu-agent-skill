/** @param {string} root @returns {Promise<void>} */
async function validateClaudeMarketplace(root) {
	const pluginPath = join(root, "claude/.claude-plugin/plugin.json");
	const marketplacePath = join(root, "claude/.claude-plugin/marketplace.json");
	assert(
		existsSync(pluginPath),
		`missing Claude plugin manifest ${pluginPath}`,
	);
	assert(
		existsSync(marketplacePath),
		`missing Claude marketplace manifest ${marketplacePath}`,
	);
	const plugin = JSON.parse(await read(pluginPath));
	const marketplace = JSON.parse(await read(marketplacePath));
	assert(plugin.name === "webcpu-agent-skill", "Claude plugin name mismatch");
	assert(
		marketplace.name === "webcpu-agent-skill",
		"Claude marketplace name mismatch",
	);
	assert(
		marketplace.owner?.name === "xsyetopz",
		"Claude marketplace owner mismatch",
	);
	assert(
		Array.isArray(marketplace.plugins) && marketplace.plugins.length === 1,
		"Claude marketplace must list one plugin",
	);
	const [entry] = marketplace.plugins;
	assert(entry.name === plugin.name, "Claude marketplace plugin name mismatch");
	assert(
		entry.version === plugin.version,
		"Claude marketplace version mismatch",
	);
	assert(entry.source === "./", "Claude marketplace source must be ./");
}

