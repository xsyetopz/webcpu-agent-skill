/** @param {string} path @returns {Promise<string>} */
async function read(path) {
	return await readFile(path, "utf8");
}

/** @param {string} dir @returns {Promise<string[]>} */
async function walk(dir) {
	const entries = await readdir(dir, { withFileTypes: true });
	const files = [];
	for (const entry of entries) {
		const path = join(dir, entry.name);
		if (entry.name === "node_modules") continue;
		if (entry.isDirectory()) files.push(...(await walk(path)));
		else if (entry.isFile()) files.push(path);
	}
	return files;
}

/** @param {string} rootPath @returns {Promise<string[]>} */
async function filesForRoot(rootPath) {
	const entry = await lstat(rootPath);
	return entry.isDirectory() ? await walk(rootPath) : [rootPath];
}

/** @param {unknown} condition @param {string} message @returns {asserts condition} */
function assert(condition, message) {
	if (!condition) throw new Error(message);
}

/** @param {string} apiText @returns {Set<string>} */
function parseApiSymbols(apiText) {
	const symbols = new Set();
	for (const match of apiText.matchAll(API_SYMBOL_RE)) {
		const symbol = match[1];
		if (symbol) symbols.add(symbol);
	}
	return symbols;
}

/** @param {string} text @returns {Set<string>} */
function importedSymbols(text) {
	const symbols = new Set();
	for (const match of text.matchAll(IMPORTED_EASEL_SYMBOL_RE)) {
		const imports = match[1];
		if (!imports) continue;
		for (const raw of imports.split(",")) {
			const name = raw
				.trim()
				.replace(TYPE_IMPORT_PREFIX_RE, "")
				.replace(EXPORT_ALIAS_RE, "");
			if (name) symbols.add(name);
		}
	}
	return symbols;
}

/** @param {string} path @returns {boolean} */
function isTextSource(path) {
	return SOURCE_TEXT_EXTENSIONS.has(path.slice(path.lastIndexOf(".")));
}

async function validateNoForbiddenSourceText() {
	for (const root of [
		"scripts",
		"source",
		"README.md",
		"CONTRIBUTING.md",
		"CODE_OF_CONDUCT.md",
		"AGENTS.md",
		"CLAUDE.md",
		"llms.txt",
		"llms-full.txt",
		"GEMINI.md",
		".github",
		".cursor",
		".rules",
		".aiassistant",
		".junie",
		".clinerules",
		".roo",
		".augment",
		"docs",
	]) {
		const rootPath = join(ROOT, root);
		const files = await filesForRoot(rootPath);
		for (const file of files) {
			if (!isTextSource(file)) continue;
			const text = await read(file);
			assert(
				!FORBIDDEN_SOURCE_TEXT_PATTERNS.some((pattern) =>
					text.toLowerCase().includes(pattern.toLowerCase()),
				),
				`forbidden local/provenance text in ${relative(ROOT, file)}`,
			);
		}
	}
}

/** @param {string} href @returns {boolean} */
function isLocalHref(href) {
	return !(URL_SCHEME_RE.test(href) || href.startsWith("#"));
}

/** @param {string} href @returns {string} */
function linkPath(href) {
	return href.split("#")[0] ?? "";
}

/** @param {string} path @returns {Promise<void>} */
async function validateLlmsManifest(path) {
	const text = await read(join(ROOT, path));
	assert(text.startsWith("# "), `${path} must start with H1`);
	assert(
		text.split("\n").some((line) => line.startsWith("> ")),
		`${path} must include blockquote summary`,
	);
	assert(LLMS_OPTIONAL_HEADING_RE.test(text), `${path} missing ## Optional`);
	assert(LLMS_H2_RE.test(text), `${path} must include H2 sections`);
	const links = [...text.matchAll(LLMS_LINK_RE)];
	assert(links.length > 0, `${path} must include markdown links`);
	let optional = false;
	for (const line of text.split("\n")) {
		if (line.startsWith("## ")) optional = line === "## Optional";
		const match = line.match(LLMS_LINE_LINK_RE);
		if (!match || optional) continue;
		const href = match[2];
		if (!(href && isLocalHref(href))) continue;
		const target = linkPath(href);
		assert(target, `${path} has empty link target`);
		assert(
			existsSync(join(ROOT, target)),
			`${path} missing linked file ${target}`,
		);
	}
}

